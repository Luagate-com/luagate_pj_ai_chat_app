/**
 * ユーザーメッセージにマッチするダミー応答を返す。
 * 複数のパターンにヒットした場合は最初にヒットしたものを採用する。
 */
export function findDummyReply(message: string): string {
  const normalized = message.toLowerCase();
  for (const pattern of dummyResponses) {
    const hit = pattern.keywords.some((kw) => normalized.includes(kw.toLowerCase()));
    if (hit) return pattern.reply;
  }
  return defaultReply;
}
